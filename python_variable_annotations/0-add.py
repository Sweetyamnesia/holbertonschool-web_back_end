#!/usr/bin/env python3
"""This module contains a function that performs
addition on two floating-point numbers"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns the result.
    Parameters :
    a (float) : the first number to add
    b (float) : the second numbers to add

    Returns:
    float: the sum of the two numbers
    """
    return a + b
